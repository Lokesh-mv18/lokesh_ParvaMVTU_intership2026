package datatypes;

public class PrimitiveDatatypes {
	public static void main(String[] args) {
		
		//byte 
		
		//byte age;
		 // age=30;
		 
		byte age=30; //same line declaration and initialization
		System.out.println("Age is:"+age);
		
		//short
		short var1=40;
		System.out.println("short: "+var1);
		
		//long
		long Var1=100;
		System.out.println("long: "+Var1);
		
		//double
		double marks1=120.50;
		System.out.println("Double: "+marks1);
		
		//int
		int total=50;
		System.out.println("int: "+total);
		
		//char
		char marks2=90;
		System.out.println("char: "+marks2);
		
		//float
		float marks3=50;
		System.out.println("float: "+marks3);
		
		//boolean
		boolean marks4=true;
		System.out.println("boolean: "+marks4);
		
		
	}

}


